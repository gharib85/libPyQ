import random
random.seed()
import numpy as np
import plots
#------------------------------------------------------------------------------------------------------------------------------------
def test_rng():
    d = 10**4
    x = np.zeros(d)
    y = np.zeros(d)
    for j in range(0,d):
        x[j] = random.random()
        y[j] = random.random()
    plots.plotScatter(x,y)
